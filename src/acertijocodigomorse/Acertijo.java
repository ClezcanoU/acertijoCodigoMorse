    private void SetImagenLabel (JLabel labelName,String ruta){
        ImageIcon morseCodeImage = new ImageIcon(ruta);
        Icon icon = new ImageIcon(morseCodeImage.getImage().getScaledInstance(labelName.getWidth(),labelName.getHeight() , Image.SCALE_DEFAULT));
        labelName.setIcon(icon);
        
        ImageIcon instruccionesImage = new ImageIcon("src/Recursos/instrucciones.png");
        Icon icon2 = new ImageIcon(instruccionesImage.getImage().getScaledInstance(jLabelImagen.getWidth(), jLabelImagen.getHeight(), Image.SCALE_DEFAULT));
        jLabelImagen.setIcon(icon2);
        this.repaint();
    }
    
    
    private void verificarCombinacion() {
        String seleccion1 = (String) comboBox1.getSelectedItem();
        String seleccion2 = (String) comboBox2.getSelectedItem();

        String combinacionCorrecta1 = "Azul";
        String combinacionCorrecta2 = "Rojo";


        if (seleccion1.equals(combinacionCorrecta1) && seleccion2.equals(combinacionCorrecta2)) {
            JOptionPane.showMessageDialog(this, "¡Combinación correcta! Avanza al próximo nivel.");
            abrirSiguienteNivel();
        } else {
            JOptionPane.showMessageDialog(this, "Combinación incorrecta. Inténtalo de nuevo.");
        }
    }

    private void abrirSiguienteNivel() {
        nivel2 n2 = new nivel2();

        n2.setVisible(true);

        this.dispose();
    }


    
    private void startButtonActionPerformed(ActionEvent evt) {
        if (inicio) {

            ImageIcon acertijoImage = new ImageIcon("src/Recursos/imagen1editar.png");
            Icon icon = new ImageIcon(acertijoImage.getImage().getScaledInstance(jLabelImagen.getWidth(), jLabelImagen.getHeight(), Image.SCALE_DEFAULT));
            jLabelImagen.setIcon(icon);
            inicio = false;
        } else {
            inicio = true;
        }
        timer.start();
    }
    
    private void actualizarTimerLabel() {
        int minutos = segundos / 60;
        int segundosRestantes = segundos % 60;

        timerLabel.setText("Tiempo: " + minutos + " : " + segundosRestantes);
    }
    
   private void actualizarLabelColor1() {
        String seleccion1 = (String) comboBox1.getSelectedItem();

        if (!seleccion1.equals("Seleccionar Color")) {
            Color colorSeleccionado = obtenerColorDesdeNombre(seleccion1);
            labelColor1.setOpaque(true);
            labelColor1.setBackground(colorSeleccionado);
        } else {
            labelColor1.setOpaque(false);
            labelColor1.setBackground(null);
        }
    }

    private void actualizarLabelColor2() {
        String seleccion2 = (String) comboBox2.getSelectedItem();

        if (!seleccion2.equals("Seleccionar Color")) {
            Color colorSeleccionado = obtenerColorDesdeNombre(seleccion2);
            labelColor2.setOpaque(true);
            labelColor2.setBackground(colorSeleccionado);
        } else {
            labelColor2.setOpaque(false);
            labelColor2.setBackground(null);
        }
    }


    private Color obtenerColorDesdeNombre(String nombreColor) {
        Map<String, Color> colores = new HashMap<>();
        colores.put("Azul", Color.BLUE);
        colores.put("Rojo", Color.RED);
        colores.put("Verde", Color.GREEN);
        colores.put("Morado", new Color(128, 0, 128));
        colores.put("Naranja", Color.ORANGE);
        colores.put("Gris", Color.GRAY);
        return colores.get(nombreColor);
    }

    
    



